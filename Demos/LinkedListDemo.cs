A Linked List is a linear data structure and 
- does not stored in contiguous memory
-  Each element in a Linked List is part of a node
- A node comprises data and a reference (or link) to the next node in the sequence. 
- The head is also an essential concept in Linked Lists. 
- It is the first node in the list and serves as a reference to the entire list. The head is a null reference if the Linked List is empty.
*/
